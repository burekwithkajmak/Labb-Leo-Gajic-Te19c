# b) - Beräkna avståndet från origo till (x, y) = (1, 1)
import math
# x-värdet på punkten
x = 1
# y-värdet på punkten
y = 1
#Pythagoras sats formel för hypotenusan och då får vi avståndet från origon till punkten
d = math.sqrt(y**2 + x**2)
print(f"Avståndet från origo till (1, 1) är {d:.2f} l.e")



