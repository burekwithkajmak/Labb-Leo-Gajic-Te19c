# a) - Beräkna avståndet från origo till (x, y) = (0.5, 0.5)
import math
# x-värdet på punkten
x = 0.5
# y-värdet på punkten
y = 0.5
#Pythagoras sats formel för hypotenusan och då får vi avståndet från origon till punkten
d = math.sqrt(y**2 + x**2)
print(f"Avståndet från origo till (0.5, 0.5) är {d:.2f} l.e")



